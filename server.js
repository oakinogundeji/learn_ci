'use strict';
//=============================================================================
/**
 * module dependencies
 */
//=============================================================================
const
    http = require('http'),
    app = require('./app');
//=============================================================================
/**
 * module config
 */
//=============================================================================
const
    port = app.get('port'),
    env = app.get('env');
//=============================================================================
/**
 * instantiate base http server
 */
//=============================================================================
const server = http.createServer(app);
//=============================================================================
/**
 * bind server to port
 */
//=============================================================================
server.listen(port, () => {
    return console.log('Travis app listening on port %d in %s mode', port, env);
});
//=============================================================================
/**
 * conditionally export module
 */
//=============================================================================
if(require.main != module) {
  module.exports = server;
}
//=============================================================================
